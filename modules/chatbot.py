# modules/chatbot.py
from typing import Dict, Any

class AnalyticalHRBot:
    """
    چتبات تحلیلی سیستم برای تفسیر خروجی مدل‌های ریاضی و ارائه راهکارهای مدیریتی به مدیر بانک
    """
    def __init__(self):
        pass

    def generate_analysis_response(self, query: str, lp_results: Dict[str, Any], ga_results: Dict[str, Any]) -> str:
        """
        یک متد تحلیل هوشمند داده‌محور که به سرعت به پرسش‌های حساسیت و ساختار بهینه‌سازی سیستم پاسخ می‌دهد.
        """
        query_lower = query.lower()
        
        # سناریو ۱: تحلیل وضعیت تخصیص شعب (LP)
        if "شعبه" in query_lower or "تخصیص" in query_lower or "بودجه" in query_lower:
            summary_text = ""
            for branch, info in lp_results['summary'].items():
                summary_text += f"- **{branch}**: تعداد {info['Total_Staff']} کارمند تخصیص یافته. بودجه باقی‌مانده: {info['Remaining_Budget']} دلار.\n"
                
            return (
                f"### 📊 تحلیل هوشمند تخصیص پرسنل به شعب:\n"
                f"بر اساس مدل برنامه‌ریزی خطی عدد صحیح (MILP) و حجم تراکنش‌ها، چیدمان بهینه انجام شد:\n\n"
                f"{summary_text}\n"
                f"**تحلیل حساسیت سریع:** هیچ‌کدام از شعب از سقف بودجه خود فراتر نرفته‌اند. "
                f"شعب با تراکنش بالاتر به طور خودکار اولویت جذب نیروهای رده بالا (مانند کارشناس اعتبارات) را داشته‌اند."
            )
            
        # سناریو ۲: تحلیل تیم‌سازی چابک تحول دیجیتال (GA)
        elif "تیم" in query_lower or "پروژه" in query_lower or "مهارت" in query_lower:
            covered = ", ".join(ga_results['skills_covered'])
            return (
                f"### 🤖 تحلیل تکاملی تیم‌سازی چابک:\n"
                f"الگوریتم ژنتیک با موفقیت به امتیاز شایستگی **{ga_results['fitness']:.2f}** دست یافت.\n\n"
                f"*   **مهارت‌های پوشش داده شده در پروژه:** {covered}\n"
                f"*   **میانگین امتیاز رفتاری تیم:** {ga_results['average_behavioral']:.2f} از ۱۰\n"
                f"*   **وضعیت تعارض:** به حداقل ممکن رسیده است زیرا انحراف معیار رفتاری اعضای منتخب ناچیز است.\n\n"
                f"**پیشنهاد هوشمند:** این تیم بالاترین هم‌افزایی را جهت اجرای فرآیند تحول دیجیتال در بانک داراست."
            )
            
        # سناریو ۳: پاسخ عمومی
        else:
            return (
                "سلام! من دستیار هوشمند تحلیلی سیستم مدیریت سرمایه انسانی بانک هستم.\n"
                "می‌توانید از من درباره **وضعیت تخصیص کارمندان به شعب شهری (تحلیل بودجه و تراکنش)** یا "
                "**کیفیت هم‌افزایی و ساختار تیم چابک پروژه تحول دیجیتال** سوال بپرسید."
            )